void InitCIS(void)
{
	XBYTE[0xFCC3] = 0x40;		//CIS_ctrl. 0x40:CPU force master clock enable, any data won't write to FIFO.
	
	SENSOR_POWER_PIN = SENSOR_PIN_ON;
	InitSensor();
}

